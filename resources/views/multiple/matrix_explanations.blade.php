    <h2 id="icv">SSV/ICV BINARIAS/CHOKE</h2>
    <p>Los Válvulas Controladoras Internas, pertenecen a la categoría de completamientos inteligentes, debido a que tienen la característica de que pueden ser obturados desde superficie por medio de líneas hidráulicas y eléctricas con el fin de tener el control de yacimiento o generar el Draw Down necesario para que todas las capas puedan fluir sin necesidad de generar flujo cruzado, de esta forma administrar el flujo del yacimiento desde superficie. Hay dos tipos de válvulas, Binarias que tienen dos posiciones, Abierta o Cerrada las cuales son utilizadas para formaciones que requieren ser producidas a flujo máximo bien porque presentan restricción de flujo natural. Por otra parte están las válvulas de Choke Discretas que tienen varias posiciones que varias de 4 u 10 agujeros (Ver Figura 4) que permiten el flujo regulado, de esta forma controlar el influjo de las formaciones. Se presenta una tabla con las diferentes opciones comerciales disponibles de las válvulas de control de fondo.</p>
