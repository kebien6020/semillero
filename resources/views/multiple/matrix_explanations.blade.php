    <h2 id="ssd">SSD</h2>
    <p>Los completamientos con BHA de producción selectiva con SSD (Sliding Sleeve Door) permiten la producción secuencial, selectiva o mezclada (de ser posible) de dos o más capas al tiempo, La producción secuencial se lleva acabo con el objetivo de evaluar el potencial de cada una de los estratos productores, y determinar parámetros como índice de productividad y características de fluido, de esta forma determinar prospectividad y posible implementación de un completamiento Dual para una producción simultánea. Este tipo de completamientos puede ser corrido con diferentes sistemas de levantamiento artificial, el más común, Bombeo Electro Sumergible (Ver Figura 1), donde por medio de una herramienta de derivación  Y-Tool permite la apertura y cierre de las camisas por medio de una operación rigless. Estos completamientos pueden ser proveídos por empresas de herramientas de completamiento, ya que requieren empaques típicos por asentamiento hidráulico y camisas de producción que pueden ser adquiridas en casi cualquier empresa de completamiento.</p>
