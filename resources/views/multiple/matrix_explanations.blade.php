    <h2 id="vrf">Inyección Selectiva/VRF</h2>
    <p>Los sistemas de inyección selectiva traen grandes ventajas en procesos de recuperación secundaria, ya que incrementan el porcentaje de recobro debido al incremento en el barrido areal que tiene (Ver Figura 2). La calibración de las válvulas permite inyectar flujo preferencial en los diferentes compartimentos, de esta forma las formaciones con menor índice de inyectividad recibirán fluido proporcional a su capacidad de flujo, y así generar un barrido más uniforme a través de la capa que está siendo inyectada, estos completamientos son corridos en yacimientos estratificados, o en formaciones con varias arenas productoras. En la industria se pueden encontrar diferentes tipos de empaques, tanto hidráulicos como mecánicos, siendo los primeros por su facilidad de asentamiento y desasentamiento más comunes en este tipo de completamientos. Empresas como Schlumberger, Completion Services o Halliburton provee un catálogo para diferentes diámetros de tubería y Casing  en los que aplica variedad de empaques Hidráulicos y tándem, con sus respectivos mandriles de inyección.</p>
