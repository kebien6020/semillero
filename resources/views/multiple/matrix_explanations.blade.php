    <h2 id="picd-aicd">PICD/AICD</h2>
    <p>Una de las nuevas herramientas implementadas en los últimos años para la administración del yacimiento han sido los Dispositivos de Control de Influjo, dispositivos diseñados con el fin de administrar el flujo de entrada, por medio de caminos preferenciales al flujo del aceite, retrasa la entrada de agua o gas, a pesar de que estos completamientos requieren un mantenimiento debido a la acumulación de solidos que puede generarse en las mallas o boquillas, en casos de campo han resultado muy efectivos en el control del frente de agua e incrementando el porcentaje de reservas. Con el transcurso de los años han surgido nuevas tecnologías que no solo permiten administrar el flujo de forma pasiva, si no dispositivos con características nuevas que permiten el cierre de flujo con la aplicación de presión en la cara interna de la herramienta, estos dispositivos de denominan Dispositivos de Control de Influjo Activos, y se consideran como una nueva tecnología que remplaza los DCI Convencionales o como una categoría de los mismos. Estos completamientos pueden encontrarse en la industria como IntelliZone perteneciente a la empresa Schlumberger y SmartWell de Halliburton, el ensamblaje de este tipo de completamiento se puede encontrar en los siguientes rangos de aplicación: </p>
